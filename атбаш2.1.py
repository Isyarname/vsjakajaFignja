al = ['а', 'б', 'в', 'г', 'д', 'е', 'ё', 'ж', 'з', 'и', 'й', 'к', 'л', 'м', 'н', 'о', 'п', 'р', 'с', 'т', 'у', 'ф', 'х', 'ц', 'ч', 'ш', 'щ', 'ъ', 'ы', 'ь', 'э', 'ю', 'я']
text = input('Введите текст\n')
text = text.split(' ')	
t = ''
	
for l in text:
	for i in l:
		for j in range(len(al)):
			if i == al[j]:
				t += al[len(al)-1-j]
	t += ' '
				
print(t)